    private void btn_RemoveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_RemoveMousePressed

        if (!value1.getText().equals("") && index1.getText().equals("")) {
            if (ArrList.findIndex(value1.getText()) != -1) {
                Object Del = ArrList.remove(value1.getText());
                printDel(Del);
            }
            show(ArrList);
        } else if (!value1.getText().equals("") && !index1.getText().equals("")) {
            if (ArrList.findIndex(value1.getText()) != -1) {
                Object Del = ArrList.remove(value1.getText(), Integer.parseInt(index1.getText()));
                printDel(Del);
            }
        } else if (value1.getText().equals("") && !index1.getText().equals("")) {
            if (Integer.parseInt(index1.getText()) > 0 && Integer.parseInt(index1.getText()) < ArrList.size()) {
                Object Del = ArrList.remove(Integer.parseInt(index1.getText()));
                printDel(Del);
            }

        } else if (value1.getText().equals("") && index1.getText().equals("")) {
            if (ArrList.size() > 0) {
                Object Del = ArrList.remove(ArrList.size() - 1);

                printDel(Del);
            }

        }

        print(ArrList);

    }//GEN-LAST:event_btn_RemoveMousePressed


